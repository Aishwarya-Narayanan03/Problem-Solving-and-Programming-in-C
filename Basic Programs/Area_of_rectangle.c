#include <stdio.h>

void main() {
    int length, breadth, area;  

    printf("Enter the length of the Rectangle: ");
    scanf("%d", &length);  

    printf("Enter the breadth of the rectangle: ");
    scanf("%d", &breadth);  

    area = length * breadth;  

    printf("Area of Rectangle: %d\n", area);  

     
}

